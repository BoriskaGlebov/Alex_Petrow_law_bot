        inf_bot = await bot.get_me()
        msg1 = f"ü§ñ <b>–ó–¥—Ä–∞–≤—Å—Ç–≤—É–π—Ç–µ! –ú–µ–Ω—è –∑–æ–≤—É—Ç {inf_bot.first_name}. –Ø —Å–µ–∫—Ä–µ—Ç–∞—Ä—å –≥—Ä—É–ø–ø—ã —é—Ä–∏–¥–∏—á–µ—Å–∫–æ–π –ø–æ–º–æ—â–∏.</b>."
        msg2 = "–î–ª—è —Å–æ—Å—Ç–∞–≤–ª–µ–Ω–∏—è –∑–∞—è–≤–∫–∏ üìÑ –º–Ω–µ –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ –∑–∞–ø–æ–ª–Ω–∏—Ç—å –∞–Ω–∫–µ—Ç—É. –≠—Ç–æ –∑–∞–π–º–µ—Ç –Ω–µ—Å–∫–æ–ª—å–∫–æ –º–∏–Ω—É—Ç."
        msg3 = "–í–∞–º –Ω—É–∂–Ω–æ –±—É–¥–µ—Ç –æ—Ç–≤–µ—Ç–∏—Ç—å –Ω–∞ –Ω–µ—Å–∫–æ–ª—å–∫–æ –≤–æ–ø—Ä–æ—Å–æ–≤ ‚ùî –∏ –ø—Ä–∏–ª–æ–∂–∏—Ç—å –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã–µ ü™™ –¥–æ–∫—É–º–µ–Ω—Ç—ã."
        # msg4 = "–í–∞–º  (–≤–∞—à–µ–º—É –∫–ª–∏–µ–Ω—Ç—É)  —É–∂–µ –∏—Å–ø–æ–ª–Ω–∏–ª–æ—Å—å üîû 18 –ª–µ—Ç? "
